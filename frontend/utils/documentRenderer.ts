/**
 * Render document content based on type
 */
export interface DocumentContent {
  content: string;
  fileType: 'docx' | 'pdf' | 'md' | 'html' | 'txt' | 'code';
  language?: string;
  isPreview?: boolean; // true if content is preview/excerpt
}

/**
 * Format document for display
 */
export function formatDocument(content: string, fileType: 'docx' | 'pdf' | 'md' | 'html' | 'txt' | 'code', language?: string): DocumentContent {
  let formatted = content;

  switch (fileType) {
    case 'docx':
      // DOCX tool returns markdown, extract clean text
      formatted = extractDocxText(content);
      break;

    case 'pdf':
      // PDF: extract available text
      formatted = extractPdfText(content);
      break;

    case 'html':
      // HTML: sanitize for display
      formatted = sanitizeHtml(content);
      break;

    case 'code':
      // Code: trim excess whitespace but preserve formatting
      formatted = content.trim();
      break;

    case 'md':
    case 'txt':
    default:
      // Markdown and text: use as-is
      formatted = content.trim();
  }

  return {
    content: formatted,
    fileType,
    language: fileType === 'code' ? language || 'plaintext' : undefined,
    isPreview: content.length > 10000, // Mark as preview if truncated
  };
}

/**
 * Generate preview text (first N chars) from content
 */
export function generatePreview(content: string, maxLength: number = 200): string {
  const trimmed = content.trim().replace(/\s+/g, ' ');
  return trimmed.length > maxLength ? `${trimmed.slice(0, maxLength)}...` : trimmed;
}

/**
 * Count words in document
 */
export function countWords(content: string): number {
  return content
    .trim()
    .split(/\s+/)
    .filter(word => word.length > 0).length;
}

/**
 * Estimate reading time (average 200 words/minute)
 */
export function estimateReadingTime(content: string): string {
  const wordCount = countWords(content);
  const minutes = Math.ceil(wordCount / 200);
  return minutes === 1 ? '1 min' : `${minutes} min`;
}

/**
 * Check if content is valid for display
 */
export function isValidContent(content: unknown): boolean {
  return typeof content === 'string' && content.trim().length > 0;
}

/**
 * Get file size formatted string
 */
export function formatFileSize(bytes: number): string {
  if (bytes === 0) return '0 B';
  const k = 1024;
  const sizes = ['B', 'KB', 'MB', 'GB'];
  const i = Math.floor(Math.log(bytes) / Math.log(k));
  return Math.round((bytes / Math.pow(k, i)) * 100) / 100 + ' ' + sizes[i];
}
