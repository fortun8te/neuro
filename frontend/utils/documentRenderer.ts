/**
 * Document Renderer — Convert various formats to displayable content
 *
 * Handles:
 * - DOCX → Markdown conversion
 * - PDF → Preview + text extraction
 * - HTML → Safe sanitization
 * - Markdown → As-is (react-markdown handles rendering)
 * - Code → With language detection
 * - Plain text → As-is
 */

/**
 * Detect file type from filename or MIME type
 */
export function detectFileType(filename: string): 'docx' | 'pdf' | 'md' | 'html' | 'txt' | 'code' {
  if (!filename) return 'txt';

  const ext = filename.split('.').pop()?.toLowerCase() || '';

  const codeExtensions = ['py', 'js', 'ts', 'tsx', 'jsx', 'java', 'cpp', 'c', 'cs', 'go', 'rs', 'rb', 'php', 'swift', 'kt', 'scala', 'sql', 'sh', 'bash', 'json', 'yaml', 'yml', 'xml', 'css', 'scss', 'html'];

  switch (ext) {
    case 'docx':
      return 'docx';
    case 'pdf':
      return 'pdf';
    case 'md':
    case 'markdown':
      return 'md';
    case 'html':
    case 'htm':
      return 'html';
    case 'txt':
    case 'text':
      return 'txt';
    default:
      return codeExtensions.includes(ext) ? 'code' : 'txt';
  }
}

/**
 * Detect programming language from file extension
 */
export function detectLanguage(filename: string): string {
  const ext = filename.split('.').pop()?.toLowerCase() || '';

  const langMap: Record<string, string> = {
    py: 'python',
    js: 'javascript',
    ts: 'typescript',
    tsx: 'typescript',
    jsx: 'jsx',
    java: 'java',
    cpp: 'cpp',
    c: 'c',
    cs: 'csharp',
    go: 'go',
    rs: 'rust',
    rb: 'ruby',
    php: 'php',
    swift: 'swift',
    kt: 'kotlin',
    scala: 'scala',
    sql: 'sql',
    sh: 'bash',
    bash: 'bash',
    json: 'json',
    yaml: 'yaml',
    yml: 'yaml',
    xml: 'xml',
    css: 'css',
    scss: 'scss',
    html: 'html',
  };

  return langMap[ext] || 'plaintext';
}

/**
 * Extract plain text from DOCX-like markdown representation
 * (DOCX generated by create_docx tool is actually well-structured markdown)
 */
export function extractDocxText(docxContent: string): string {
  // DOCX tool returns markdown, so we can use it as-is for display
  // Remove any embedded encoding markers if present
  return docxContent
    .replace(/\[Document\]:\s*<>/g, '') // Remove document markers
    .replace(/\[.*?]:\s*<.*?>/g, '') // Remove reference-style link definitions
    .trim();
}

/**
 * Convert HTML to safe displayable content (basic sanitization)
 * For production, consider using DOMPurify
 */
export function sanitizeHtml(html: string): string {
  // Basic sanitization: remove script tags and event handlers
  let sanitized = html
    .replace(/<script\b[^<]*(?:(?!<\/script>)<[^<]*)*<\/script>/gi, '') // Remove script tags
    .replace(/on\w+\s*=\s*["'][^"']*["']/gi, '') // Remove event handlers
    .replace(/on\w+\s*=\s*[^\s>]*/gi, ''); // Remove unquoted event handlers

  return sanitized;
}

